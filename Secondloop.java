public class Secondloop{




	public static void main (String [] args){

		
	for(int firstIteration = 1; firstIteration  <= 6;firstIteration ++){

		System.out.printf("%d ",firstIteration );
		if(firstIteration == 6)
		System.out.println(" ");

	
				
};
for(int secondIteration = 1; secondIteration  <= 5;secondIteration ++){

		System.out.printf("%d ",secondIteration );
		if(secondIteration == 5)
		System.out.println(" ");

	
				
};
for(int thirdIteration = 1; thirdIteration  <= 4;thirdIteration ++){

		System.out.printf("%d ",thirdIteration );
		if(thirdIteration == 4)
		System.out.println(" ");

	
				
};
for(int fourthIteration = 1; fourthIteration  <= 3;fourthIteration ++){

		System.out.printf("%d ",fourthIteration );
		if(fourthIteration == 3)
		System.out.println(" ");

	
				
};

for(int fifthIteration = 1; fifthIteration  <= 2;fifthIteration ++){

		System.out.printf("%d ",fifthIteration );
		if(fifthIteration == 2)
		System.out.println(" ");

	
				
};
for(int sixthIteration = 1; sixthIteration  <= 1;sixthIteration ++){

		System.out.printf("%d ",sixthIteration );
		if(sixthIteration == 1)
		System.out.println(" ");

	
				
};




}






}